# Program that prints the variables type and value

# Setting up the value for the variables
age = 10
height = 1.80
isMarried = False
name = 'John' 
favoriteFood = ['rice', 'beans', 'curry', 'soup', 'pasta', 'pizza']

# Code used to print the information in the screen.
print("Variable {} is of type {} and value {} ". format('age', type(age), age))
print("Variable {} is of type {} and value {} ". format('height', type(height), height))
print("Variable {} is of type {} and value {} ". format('isMarried', type(isMarried), isMarried))
print("Variable {} is of type {} and value {} ". format('name', type(name), name))
print("Variable {} is of type {} and value {} ". format('favoriteFood', type(favoriteFood), favoriteFood))